    private void FoodMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_FoodMouseClicked
       String eid = eidno.getText();
       
       
       String Desig = null;
       String Eid= null;
        
        if(eid.isEmpty())
        {
            showMessageDialog(null,"Enter your ID Number ");
        }
        else if(found(eid)==0)
        {
            showMessageDialog(null,"You already selected");
        }
        else
        {
            try {
            
            String sql = "SELECT * FROM employee Where eid = '"+eid+"'";
            pst1 = conn.prepareStatement(sql);
            rst1 = pst1.executeQuery();
           
            while(rst1.next())
            {
               Eid = rst1.getString("eid");
               Desig = rst1.getString("desig");
            }
            
                if(eid.equals(Eid))
                {
                    if(Desig.equals("Manager") || Desig.equals("Supervisor"))
                    {
                        C c = new C();
                        c.password.setText(eid);
                        c.setVisible(true);
                        this.dispose();
                        
                    }
                    else if(Desig.equals("Order Manager")|| Desig.equals("Delivery Manager") || Desig.equals("Maintainer") || Desig.equals("Cashier") || Desig.equals("Cheff"))
                    {
                         B b = new B();
                        b.setVisible(true);
                        this.dispose();
                    }
                    else if(Desig.equals("Worker"))
                    {
                        A a = new A();
                        a.setVisible(true);
                        this.dispose(); 
                    }
                    
                }
                else
                    {
                        showMessageDialog(null,"Wrong ID Number");
                        eidno.setText(null);
                    
                    }
                
            
            
        } catch (Exception e) {
            
            System.out.println(e);
        }
            
        }
       
    }//GEN-LAST:event_FoodMouseClicked

