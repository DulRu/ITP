    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        //add
        String mtype = AddNewMachineType.getText();
        
        try {
            String d = "INSERT INTO mtype (mtype) VALUES ('"+mtype+"')";
            pst = con.prepareStatement(d);
            pst.execute();

            
            JOptionPane.showMessageDialog(null, "Succesfully added.");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Error" , JOptionPane.ERROR_MESSAGE);
        }
        
        AddNewMachineType.setText(null);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void DeleteMachineTypeFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_DeleteMachineTypeFocusGained
     
        DeleteMachineType.removeAllItems();
        
        //set values to machine type combo box from db
        
        try {
            String sql = "SELECT DISTINCT mtype FROM mtype";
            pst = con.prepareStatement(sql);
            
            rs = pst.executeQuery();
            
            while(rs.next()){
            
                String mtype1 = rs.getString("mtype");
                DeleteMachineType.addItem(mtype1);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Error" , JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_DeleteMachineTypeFocusGained

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        int x = JOptionPane.showConfirmDialog(null, "Do you want to delete this?");

        if(x==0){
            String mtype1 = DeleteMachineType.getSelectedItem().toString();

            String sql = "DELETE FROM mtype WHERE mtype = '"+mtype1+"'";

            try {
                pst = con.prepareStatement(sql);
                pst.execute();
                
                JOptionPane.showMessageDialog(null, "Succesfully deleted.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e, "Error" , JOptionPane.ERROR_MESSAGE);
            }

            
        }

        DeleteMachineType.setSelectedIndex(0);
    }//GEN-LAST:event_jButton2ActionPerformed

