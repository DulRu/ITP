    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int x = showConfirmDialog(null, "Do you want to delete this");

        if(x==0){
            String btype1 = DeleteBillType.getSelectedItem().toString();

            String sql = "DELETE FROM billtype WHERE Btype = '"+btype1+"'";

            try {
                pst = con.prepareStatement(sql);
                pst.execute();
                
                JOptionPane.showMessageDialog(null, "Succesfully deleted.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e, "Error" , JOptionPane.ERROR_MESSAGE);
            }

            
        }

        DeleteBillType.setSelectedIndex(0);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void DeleteBillTypeFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_DeleteBillTypeFocusGained
        DeleteBillType.removeAllItems();
        
        //set values to machine type combo box from db
        
        try {
            String sql = "SELECT DISTINCT btype FROM billtype";
            pst = con.prepareStatement(sql);
            
            rs = pst.executeQuery();
            
            while(rs.next()){
            
                String btype1 = rs.getString("btype");
                DeleteBillType.addItem(btype1);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Error" , JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_DeleteBillTypeFocusGained

