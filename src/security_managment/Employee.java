        emp=empp.getText();
        
          String sql="select eid,status from employee where eid=?";
        try{
            pst=con.prepareStatement(sql);
            pst.setString(1,empp.getText());
            rs=pst.executeQuery();
            
           if(rs.next())
            {
                if(emp.equals(rs.getString("eid")))
                {
                    stat.setText(rs.getString("status"));
                }
                
            }
                else
                {
                    stat.setText("not qulified");
                }
            

        }
        
         catch(Exception e)
        {
                             JOptionPane.showMessageDialog(null,"Invalid Employee ID");   
   
        }
        
        reset();           
        
      
        
