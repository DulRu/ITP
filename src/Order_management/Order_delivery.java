    private void jLabel26MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel26MouseClicked
        //Serach invoice data and load DB data to TextFields
        String cus = jTextField23.getText(); 
        
       String FName;
        String LName;
        String add1;
        String add2;
        String city;
        String state;
        String zip;
        String country;
        String company;
        String phone ;
        String email;
        String fax ;
            //Search customer
             try 
            {
                String sql="SELECT * FROM customer WHERE id LIKE '"+cus+"'";
                pst =con.prepareStatement(sql);
                rs = pst.executeQuery();
              if(rs.next()){
                  rs=pst.executeQuery();
                  while(rs.next()){
                      FName=rs.getString("fname");
                      LName=rs.getString("lname");
                      add1=rs.getString("add1");
                      add2=rs.getString("add2");
                      city=rs.getString("city");
                      state=rs.getString("state");
                      zip=rs.getString("zip");
                      country=rs.getString("country");
                      company=rs.getString("company_name");
                      phone=rs.getString("phone");
                      email=rs.getString("email");
                      fax=rs.getString("fax");
                     
                      
                      jTextField2.setText(FName);
                      jTextField3.setText(LName);
                      jTextField4.setText(add1);
                      jTextField5.setText(add2);
                      jTextField6.setText(city);
                      jTextField1.setText(state);
                      jTextField7.setText(zip);
                      jTextField8.setText(country);
                      jTextField9.setText(company);
                      jTextField10.setText(phone);
                      jTextField11.setText(email);
                     jTextField24.setText(fax);
                      
                
                  }
                  
              }
              
              else{
                  JOptionPane.showMessageDialog(null,"Not found");
              }
                
              
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
    }//GEN-LAST:event_jLabel26MouseClicked

