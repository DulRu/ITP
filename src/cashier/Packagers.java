    public int isLetter(String m) {
        int a = 1;

        if (!m.isEmpty()) {
            for (int i = 0; i < m.length(); i++) {
                if (!Character.isLetter(m.charAt(i))) {
                    a = 0;
                }

            }
        }
        return a;
    }

    public int width(String m) {
        int a = 1;

        if (!m.isEmpty()) {
            if (m.length() >= 15) {
                a = 0;
            }
        }
        return a;
    }

